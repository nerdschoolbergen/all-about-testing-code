package exercise4;

/**
 * Represents the decimal value and symbol for a roman numeral. E.g, 9 = IX
 * <p>
 * You won't need this class until the last task in exercise 4
 */
public class RomanNumeral {
    final int value;
    final String symbol;

    public RomanNumeral(int value, String symbol) {
        this.value = value;
        this.symbol = symbol;
    }
}
